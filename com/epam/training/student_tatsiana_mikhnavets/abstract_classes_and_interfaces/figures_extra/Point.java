package com.epam.training.student_tatsiana_mikhnavets.abstract_classes_and_interfaces.figures_extra;

public class Point {
    private double x;
    private double y;

    public Point(final double x, final double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

}
